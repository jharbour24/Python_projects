def validate_candidate(candidate: MemeCandidate) -> tuple[bool, Optional[str]]:
    """
    Validate a candidate meets all hard constraints.

    Returns:
        (is_valid, error_message)
    """
    # Check caption length
    word_count = len(candidate.caption.split())
    if word_count > 14:
        return False, f"Caption too long: {word_count} words (max 14)"

    # Check template is valid
    valid_templates = ["drake", "distracted_boyfriend", "tweet_screenshot", "two_panel", "top_text_bottom_text"]
    if candidate.visual_template not in valid_templates:
        return False, f"Invalid template: {candidate.visual_template}"

    # Check tone is valid
    valid_tones = ["dry", "slightly unhinged", "coy"]
    if candidate.tone not in valid_tones:
        return False, f"Invalid tone: {candidate.tone}"

    # Check has local hook
    if not candidate.local_hook or len(candidate.local_hook) < 5:
        return False, "Missing or too-short local_hook"

    return True, None
