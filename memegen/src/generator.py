def _generate_caption_from_snippet(
    template: str,
    tone: str,
    snippet: LocalSnippet
) -> str:
    """
    Generate a caption using template and snippet.
    In production, this would call an LLM.
    """
    # Simple rule-based generation for demo
    if template == "drake":
        reject = random.choice(REJECT_PHRASES)
        prefer = f"{snippet.text[:40]}..."  # Use snippet
        parts = [reject, prefer]
        caption = " // ".join(parts)

    elif template == "two_panel":
        before = random.choice(BEFORE_PHRASES)
        after = snippet.text[:50]  # Use snippet
        caption = f"before: {before} // after: {after}"

    elif template == "tweet_screenshot":
        # Incorporate snippet into statement
        caption = f"{snippet.text[:80]}"

    elif template == "top_text_bottom_text":
        top = "brooklyn nightlife"
        bottom = snippet.text[:60]
        caption = f"{top} // {bottom}"

    else:  # distracted_boyfriend
        caption = f"me: existing // also me: {snippet.text[:60]}"

    # Ensure ≤ 14 words
    words = caption.split()
    if len(words) > 14:
        caption = " ".join(words[:14])

    return caption


